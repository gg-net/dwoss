    private static class StockCellRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
            JLabel label = (JLabel)super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
            if ( value == null ) return label;
            if ( value instanceof Stock ) label.setText(((Stock)value).getName());
            return label;
        }
    }

    private boolean ok = false;

    /** Creates new form ComboBoxDialog */
    public StockDialog(java.awt.Window parent, Stock[] ts) {
        super(parent);
        setModalityType(ModalityType.APPLICATION_MODAL);
        initComponents();
        setLocationRelativeTo(parent);
        selectionBox.setModel(new DefaultComboBoxModel<>(ts));
        selectionBox.setRenderer(new StockCellRenderer());
        // Close the dialog when Esc is pressed
        String cancelName = "cancel";
        InputMap inputMap = getRootPane().getInputMap(JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        inputMap.put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), cancelName);
        ActionMap actionMap = getRootPane().getActionMap();
        actionMap.put(cancelName, new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent e) {
                doClose(false);
            }
        });
    }

    public void setSelection(Stock s) {
        selectionBox.setSelectedItem(s);
    }

    public void setSelection(PicoStock ps) {
        for (int i = 0; i < selectionBox.getItemCount(); i++) {
            Stock elem = selectionBox.getItemAt(i);
            if ( elem.getId() == ps.id ) selectionBox.setSelectedIndex(i);
        }
    }

    public Stock getSelection() {
        return (Stock)selectionBox.getSelectedItem();
    }

    public boolean isOk() {
        return ok;
    }

