    @Override
    public boolean pre(CloseType type) {
        if ( type == CloseType.OK && settlements.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Mindestens eine Zahlungsart muss hinterlegt sein.");
            return false;
        }
        return true;
    }

